# replace <lhs> with <rhs> on the rightmost end of string. 
def rreplace( s, old, new ):
    li = s.rsplit( old, 1 )
    return new.join( li )
#--------------------------------------------------------------------------------
def engVirgin_parivRtyatAm( ghaTanaAkS, zRGkhalA ):
    for i in range( len( engVirgin[ ghaTanaAkS ] )):
        anitya_zRG = rreplace(zRGkhalA, engVirgin[ghaTanaAkS][i][0], engVirgin[ghaTanaAkS][i][1] )
        if anitya_zRG != zRGkhalA:
            return anitya_zRG
    return zRGkhalA
